    private boolean checkData(){
        if(dbName.getText() == null || dbName.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "数据库名不能为空");
            return false;
        }else if(userNameForMysql.getText() == null || userNameForMysql.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "用户名不能为空");
            return false;
        }
        return true;
    }

