    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        // TODO add your handling code here:
        if(Consts.file == null){
            JOptionPane.showMessageDialog(rootPane, "文件路径不能为空");
            return ;
        }
        if(Consts.file != null){
            if(Consts.file.isDirectory()){
                JOptionPane.showMessageDialog(rootPane, "不可以选择文件夹");
                return ;
            }
            this.tishi.setOpaque(true);
            DbDriver.initDb(dbName.getText(), userNameForMysql.getText(), userPswdForMysql.getText());
            String msg = "非正常程序结束，请联系开发人员。";
            try{
                msg = ModelOp.insertData(Consts.file.getAbsolutePath());
            }catch(Exception ex){
                ex.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "出现错误，请测试数据库连接，如果多次出现，请联系开发人员");
                System.exit(1);
            }
            JOptionPane.showMessageDialog(rootPane, msg);
        }
    }//GEN-LAST:event_button3ActionPerformed

